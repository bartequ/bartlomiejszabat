<h1> O grze</h1>
Warcraft III: WBE to jedna z najbardziej oczekiwanych premier lata 2017 roku. Gra powstała z ogromnym zaangażowaniem jego twórców, którzy włożyli w ten projekt całe swoje serce.WBE przenosi graczy do uniwersum znanego z wielu gier o tematyce fantastyki: Warcrafta 3, Heroes of Might and Magic a także DOTA 2. W grze gracz staje przed zadaniem rozbudowy swojego królestwa, a następnie podboju świata. W WBE nie można ograniczać się do zamknięcia się w jednym mieście i obunkrowania go, zasoby nie są nieskończone co zmusza gracza do ciągłej ekspansji. Prowadzić królestwo do podboju możemy przy pomocy ogromu bohaterów którzy występowali w wielu znanych tytułach, wraz z kolejnymi walkami bohaterowie nabierają umiejętności i mają coraz większy wpływ na rezultat starcia.
